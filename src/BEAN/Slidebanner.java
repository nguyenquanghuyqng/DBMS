package BEAN;

public class Slidebanner {

	private String slidebannername;
	private String slidebannercontent;
	private String slidebannerimage;

	public String getSlidebannername() {
		return slidebannername;
	}

	public void setSlidebannername(String slidebannername) {
		this.slidebannername = slidebannername;
	}

	public String getSlidebannercontent() {
		return slidebannercontent;
	}

	public void setSlidebannercontent(String slidebannercontent) {
		this.slidebannercontent = slidebannercontent;
	}

	public String getSlidebannerimage() {
		return slidebannerimage;
	}

	public void setSlidebannerimage(String slidebannerimage) {
		this.slidebannerimage = slidebannerimage;
	}
}

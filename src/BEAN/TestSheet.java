package BEAN;

public class TestSheet {
	private int testid;
	private String testname;
	private int numberListen;
	private int numberRead;
	private int time;

	public int getTestid() {
		return testid;
	}

	public void setTestid(int testid) {
		this.testid = testid;
	}

	public String getTestname() {
		return testname;
	}

	public void setTestname(String testname) {
		this.testname = testname;
	}

	public int getNumberListen() {
		return numberListen;
	}

	public void setNumberListen(int numberListen) {
		this.numberListen = numberListen;
	}

	public int getNumberRead() {
		return numberRead;
	}

	public void setNumberRead(int numberRead) {
		this.numberRead = numberRead;
	}

	public int getTime() {
		return time;
	}

	public void setTime(int time) {
		this.time = time;
	}

}
